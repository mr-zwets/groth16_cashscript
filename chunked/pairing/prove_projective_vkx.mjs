// Sparse integer polynomials make the formula checks exact: these are identities
// over Z, so they hold after reduction in Fp. The curve reducer substitutes
// PY^2=PX^3+3PZ^6 and QY^2=QX^3+3 to prove both generated operations preserve
// the curve for every on-curve input, not merely for selected coordinates.
const VARIABLE_COUNT = 5;
const polyMonomial = (exponents, coefficient = 1n) => coefficient === 0n
  ? new Map()
  : new Map([[exponents.join(','), coefficient]]);
const polyConstant = (value) => polyMonomial(Array(VARIABLE_COUNT).fill(0), value);
const polyVariable = (index) => {
  const exponents = Array(VARIABLE_COUNT).fill(0);
  exponents[index] = 1;
  return polyMonomial(exponents);
};
const polyAdd = (...polynomials) => {
  const result = new Map();
  polynomials.forEach((polynomial) => {
    polynomial.forEach((coefficient, monomial) => {
      const sum = (result.get(monomial) ?? 0n) + coefficient;
      if (sum === 0n) result.delete(monomial);
      else result.set(monomial, sum);
    });
  });
  return result;
};
const polyScale = (polynomial, scalar) => {
  const result = new Map();
  polynomial.forEach((coefficient, monomial) => {
    const product = coefficient * scalar;
    if (product !== 0n) result.set(monomial, product);
  });
  return result;
};
const polySubtract = (left, right) => polyAdd(left, polyScale(right, -1n));
const polyMultiply = (left, right) => {
  const result = new Map();
  left.forEach((leftCoefficient, leftMonomial) => {
    const leftExponents = leftMonomial.split(',').map(Number);
    right.forEach((rightCoefficient, rightMonomial) => {
      const rightExponents = rightMonomial.split(',').map(Number);
      const monomial = leftExponents.map((value, index) => value + rightExponents[index]).join(',');
      const coefficient = (result.get(monomial) ?? 0n) + leftCoefficient * rightCoefficient;
      if (coefficient === 0n) result.delete(monomial);
      else result.set(monomial, coefficient);
    });
  });
  return result;
};
const polyPower = (polynomial, exponent) => {
  let result = polyConstant(1n);
  let base = polynomial;
  while (exponent !== 0) {
    if (exponent & 1) result = polyMultiply(result, base);
    base = polyMultiply(base, base);
    exponent >>= 1;
  }
  return result;
};
const assertPolynomialEqual = (left, right, message) => {
  assert(left.size === right.size && [...left].every(([monomial, coefficient]) =>
    right.get(monomial) === coefficient), message);
};
const [PX, PY, PZ, QX, QY] = Array.from({ length: VARIABLE_COUNT }, (_, index) => polyVariable(index));
const reduceOnCurves = (polynomial) => {
  let result = new Map();
  polynomial.forEach((coefficient, monomial) => {
    const exponents = monomial.split(',').map(Number);
    const yPairs = Math.floor(exponents[1] / 2);
    const qyPairs = Math.floor(exponents[4] / 2);
    exponents[1] %= 2;
    exponents[4] %= 2;
    let term = polyMonomial(exponents, coefficient);
    term = polyMultiply(term, polyPower(polyAdd(
      polyPower(PX, 3), polyScale(polyPower(PZ, 6), 3n),
    ), yPairs));
    term = polyMultiply(term, polyPower(polyAdd(
      polyPower(QX, 3), polyConstant(3n),
    ), qyPairs));
    result = polyAdd(result, term);
  });
  return result;
};
const rawMixedAdd = (h, rr, x, y, z) => {
  const hh = polyPower(h, 2);
  const hhh = polyMultiply(h, hh);
  const vv = polyMultiply(x, hh);
  const nx = polySubtract(polySubtract(polyPower(rr, 2), hhh), polyScale(vv, 2n));
  return [
    nx,
    polySubtract(polyMultiply(rr, polySubtract(vv, nx)), polyMultiply(y, hhh)),
    polyMultiply(z, h),
  ];
};

const doubleA = polyPower(PX, 2);
const doubleB = polyPower(PY, 2);
const doubleC = polyPower(doubleB, 2);
const doubleD = polyScale(polyMultiply(PX, doubleB), 4n);
const doubleE = polyScale(doubleA, 3n);
const doubleX = polySubtract(polyPower(doubleE, 2), polyScale(doubleD, 2n));
const doubleY = polySubtract(
  polyMultiply(doubleE, polySubtract(doubleD, doubleX)),
  polyScale(doubleC, 8n),
);
const doubleZ = polyScale(polyMultiply(PY, PZ), 2n);
const expectedTangentX = polySubtract(polyPower(doubleE, 2),
  polyScale(polyMultiply(PX, polyPower(PY, 2)), 8n));
const expectedTangentY = polySubtract(
  polyMultiply(doubleE, polySubtract(polyScale(polyMultiply(PX, polyPower(PY, 2)), 4n), doubleX)),
  polyScale(polyPower(PY, 4), 8n),
);
assertPolynomialEqual(doubleX, expectedTangentX, 'Jacobian double X is not the tangent-law numerator');
assertPolynomialEqual(doubleY, expectedTangentY, 'Jacobian double Y is not the tangent-law numerator');
assert(reduceOnCurves(polySubtract(
  polyPower(doubleY, 2),
  polyAdd(polyPower(doubleX, 3), polyScale(polyPower(doubleZ, 6), 3n)),
)).size === 0, 'Jacobian double does not preserve the BN254 curve equation');

const zSquared = polyPower(PZ, 2);
const h = polySubtract(polyMultiply(QX, zSquared), PX);
const rr = polySubtract(polyMultiply(polyMultiply(QY, zSquared), PZ), PY);
const [addX, addY, addZ] = rawMixedAdd(h, rr, PX, PY, PZ);
const secantX = polySubtract(
  polySubtract(polyPower(rr, 2), polyMultiply(PX, polyPower(h, 2))),
  polyMultiply(polyMultiply(QX, zSquared), polyPower(h, 2)),
);
const secantY = polySubtract(
  polyMultiply(rr, polySubtract(polyMultiply(PX, polyPower(h, 2)), addX)),
  polyMultiply(PY, polyPower(h, 3)),
);
assertPolynomialEqual(addX, secantX, 'mixed-add X is not the secant-law numerator');
assertPolynomialEqual(addY, secantY, 'mixed-add Y is not the secant-law numerator');
assert(reduceOnCurves(polySubtract(
  polyPower(addY, 2),
  polyAdd(polyPower(addX, 3), polyScale(polyPower(addZ, 6), 3n)),
)).size === 0, 'mixed addition does not preserve the BN254 curve equation');

const zero = polyConstant(0n);
const [equalX, equalY, equalZ] = rawMixedAdd(zero, zero, PX, PY, PZ);
assert(equalX.size === 0 && equalY.size === 0 && equalZ.size === 0,
  'equal-point raw addition does not produce the fallback sentinel');
const [inverseX, inverseY, inverseZ] = rawMixedAdd(
  zero, polyScale(PY, -2n), PX, PY, PZ,
);
assertPolynomialEqual(inverseX, polyScale(polyPower(PY, 2), 4n),
  'inverse-add X specialization changed');
assertPolynomialEqual(inverseY, polyScale(polyPower(PY, 3), 8n),
  'inverse-add Y is not -rr^3');
assert(inverseZ.size === 0 && 8n % P !== 0n,
  'inverse addition does not produce nonzero-Y projective infinity');

// Universal induction over every generated transition:
//
//  * Identity/no-add: (0,1,0) starts with nonzero Y; a skipped operation keeps it.
//  * Add to infinity: jacAddAffine returns the selected finite table point.
//  * Double: a finite state has Y,Z != 0, so Z'=2YZ != 0. The result remains
//    in the odd prime-order subgroup and therefore cannot have affine Y=0.
//  * Equal add: h=rr=0 makes raw Y=0, and the exact generated branch above
//    redirects to the proven doubling formula.
//  * Inverse add: h=0 and rr=-2Y give Z'=0 and Y'=8Y^3 != 0.
//  * Ordinary add: h!=0 gives Z'=Zh!=0. The finite result remains in the
//    subgroup, where Y=0 would be nontrivial 2-torsion; gcd(2,R)=1 excludes it.
//
// These cases exhaust Z=0, and for Z!=0 the relations h=0 with rr=0,
// h=0 with rr!=0, and h!=0. Thus every reachable state has invertible Y.

const fixedMiller = Fp12.mul(
  singlePairMiller(pairsFor([0n, 0n], undefined, { msmOnly: true })[1]).f,
  singlePairMiller({ P: vk.ic[0], Q: vk.gamma }).f,
);

// Deterministic end-to-end checks for projective normalization and IC0 folding.
let random = 0x6a09e667f3bcc909n;
for (let index = 0; index < 128; index++) {
  random ^= random << 13n;
  random ^= random >> 7n;
  random ^= random << 17n;
  const in0 = ((random % R) + R) % R;
  random ^= random << 13n;
  random ^= random >> 7n;
  random ^= random << 17n;
  const in1 = ((random % R) + R) % R;
  const scalars = glvDecomposeJoint(in0, in1);
  const [X, Y, Z] = vkxGlvSplitStateAt(...scalars, VKXGLV_SPLIT_ITERS);
  const msm = pairsFor([in0, in1], undefined, { msmOnly: true })[2].P;
  if (Y === 0n) throw new Error(`projective accumulator has zero Y at ${index}`);
  const yInv = Fp.inv(Y);
  const z2 = Fp.sqr(Z);
  const u = Fp.neg(Fp.mul(Fp.mul(X, Z), yInv));
  const v = Fp.neg(Fp.mul(Fp.mul(z2, Z), yInv));
  if (Fp.add(Fp.mul(u, Y), Fp.mul(X, Z)) !== 0n ||
      Fp.add(Fp.mul(v, Y), Fp.mul(z2, Z)) !== 0n) {
    throw new Error(`projective cross-multiplication mismatch at ${index}`);
  }

  if (Z === 0n) {
    if (!msm.equals(bn254.G1.Point.ZERO)) throw new Error(`projective infinity mismatch at ${index}`);
    if (u !== 0n || v !== 0n) throw new Error(`projective infinity is not normalized to zero at ${index}`);
  } else {
    const affine = msm.toAffine();
    const affineYInv = Fp.inv(affine.y);
    if (u !== Fp.neg(Fp.mul(affine.x, affineYInv)) || v !== Fp.neg(affineYInv)) {
      throw new Error(`projective unit-line coordinates mismatch at ${index}`);
    }
  }

  const original = millerBatchOps(pairsFor([in0, in1])).boundary;
  const projective = millerFusedAffineOps(
    pairsFor([in0, in1], undefined, { msmOnly: true }),
    Fp12.ONE,
    Fp12.ONE,
    { unitLines: true, fixedMiller },
  ).boundary;
  if (!Fp12.eql(Fp12.finalExponentiate(original), Fp12.finalExponentiate(projective))) {
    throw new Error(`IC0/projective pairing mismatch at ${index}`);
  }
}

const infinityState = vkxGlvSplitStateAt(
  ...glvDecomposeJoint(0n, 0n), VKXGLV_SPLIT_ITERS,
);
if (infinityState[0] !== 0n || infinityState[1] !== 1n || infinityState[2] !== 0n) {
  throw new Error('canonical zero-input MSM is not the (0,1,0) projective infinity');
}
const originalInfinity = millerBatchOps(pairsFor([0n, 0n])).boundary;
const projectiveInfinity = millerFusedAffineOps(
  pairsFor([0n, 0n], undefined, { msmOnly: true }),
  Fp12.ONE,
  Fp12.ONE,
  { unitLines: true, fixedMiller },
).boundary;
if (!Fp12.eql(Fp12.finalExponentiate(originalInfinity), Fp12.finalExponentiate(projectiveInfinity))) {
  throw new Error('MSM-at-infinity pairing mismatch');
}

