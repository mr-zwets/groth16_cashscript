// Universal certificate for the grouped GLV accumulator invariant used by the
// projective GLV-to-Miller handoff.
//
// The generated MSM starts at (0,1,0), conditionally doubles a finite Jacobian
// accumulator, then adds zero to two points from fixed affine tables. This
// certificate binds the argument below to those exact generated kernels and all
// 30 serialized table entries. It proves by an exhaustive geometric case split
// that every reachable accumulator has Y != 0, including the projective
// infinity states produced by inverse addition. The deterministic end-to-end
// cases at the end are additional checks, not the basis of the universal claim.
