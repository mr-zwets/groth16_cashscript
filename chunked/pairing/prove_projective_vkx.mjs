let checkedTableEntries = 0;
for (let group = 0; group < VKXGLV_SPLIT_GROUPS; group += 1) {
  const shift = 1n << BigInt(group * VKXGLV_SPLIT_ITERS);
  for (let mask = 1; mask <= TABLE_ENTRIES; mask += 1) {
    const offset = (group * TABLE_ENTRIES + mask - 1) * TABLE_ENTRY_BYTES;
    const x = fromLe(tableBytes.subarray(offset, offset + 32));
    const y = fromLe(tableBytes.subarray(offset + 32, offset + 64));
    assert(x > 0n && x < P && y > 0n && y < P,
      `table point ${group}:${mask} is not canonical finite/nonzero-coordinate`);
    const tablePoint = bn254.G1.Point.fromAffine({ x, y });
    tablePoint.assertValidity();
    assert(!tablePoint.equals(bn254.G1.Point.ZERO) && tablePoint.isTorsionFree(),
      `table point ${group}:${mask} is not a finite subgroup point`);
    let expected = bn254.G1.Point.ZERO;
    lanePoints.forEach((point, lane) => {
      if (mask & (1 << lane)) expected = expected.add(point.multiplyUnsafe(shift));
    });
    assert(tablePoint.equals(expected), `serialized table mismatch at ${group}:${mask}`);
    checkedTableEntries += 1;
  }
}
