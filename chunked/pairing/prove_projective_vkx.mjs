const gcd = (left, right) => {
  while (right !== 0n) [left, right] = [right, left % right];
  return left < 0n ? -left : left;
};
const modPow = (base, exponent, modulus) => {
  let result = 1n;
  base %= modulus;
  while (exponent !== 0n) {
    if (exponent & 1n) result = result * base % modulus;
    base = base * base % modulus;
    exponent >>= 1n;
  }
  return result;
};
const provenPrimes = new Set();
const provePrime = (candidate) => {
  if (provenPrimes.has(candidate)) return;
  assert(candidate >= 2n, `${candidate} is not prime`);
  if (candidate < 1_000_000_000n) {
    if (candidate !== 2n) {
      assert(candidate % 2n !== 0n, `${candidate} has factor 2`);
      for (let divisor = 3n; divisor * divisor <= candidate; divisor += 2n) {
        assert(candidate % divisor !== 0n, `${candidate} has factor ${divisor}`);
      }
    }
  } else {
    const factors = pocklingtonFactors.get(candidate);
    assert(factors !== undefined, `missing Pocklington factors for ${candidate}`);
    let knownFactor = 1n;
    factors.forEach((factor) => {
      provePrime(factor);
      assert((candidate - 1n) % factor === 0n, `${factor} does not divide ${candidate}-1`);
      knownFactor *= factor;
    });
    assert(knownFactor * knownFactor > candidate,
      `known factor of ${candidate}-1 does not exceed sqrt(${candidate})`);
    assert(modPow(2n, candidate - 1n, candidate) === 1n,
      `base-2 Fermat congruence failed for ${candidate}`);
    factors.forEach((factor) => {
      const pocklingtonTerm = (modPow(2n, (candidate - 1n) / factor, candidate) + candidate - 1n) % candidate;
      assert(gcd(pocklingtonTerm, candidate) === 1n,
        `base-2 Pocklington gcd failed for ${candidate}, factor ${factor}`);
    });
  }
  provenPrimes.add(candidate);
};
